package AM11to20;

public class QuoRemain {

    public static void main(String[] args) {

        int i = 5;
        int j = 3;

        int quotient = i/j;
        int remainder = i%j;

        System.out.println("The Quotient When "+i+" / "+j+" is :"+quotient);
        System.out.println("The Remainder When "+i+" % "+j+" is: "+remainder);
    }
}
